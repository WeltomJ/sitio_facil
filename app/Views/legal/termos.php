    <h2 class="h5 fw-bold mt-4 mb-3" id="s3">3. Cadastro e Conta</h2>
    <p><strong>3.1.</strong> O uso completo da Plataforma exige cadastro prévio, mediante fornecimento de nome completo, e-mail, CPF ou CNPJ, senha e telefone (opcional).</p>
    <p><strong>3.2.</strong> O Usuário declara que as informações fornecidas são verdadeiras, atualizadas e completas, sendo responsável civil e criminalmente por informações falsas ou inexatas.</p>
    <p><strong>3.3.</strong> O cadastro é pessoal e intransferível. O Usuário é o único responsável por manter a confidencialidade de suas credenciais de acesso e por todas as atividades realizadas em sua conta.</p>
    <p><strong>3.4.</strong> O Usuário deve notificar imediatamente o Sítio Fácil caso suspeite de acesso não autorizado à sua conta, pelo e-mail <strong>contato@sitiofacil.com.br</strong>.</p>
    <p><strong>3.5.</strong> É vedado o cadastro de menores de 18 anos sem representação ou assistência dos pais ou responsáveis legais. Ao se cadastrar, o Usuário declara ter pelo menos 18 anos de idade ou estar devidamente representado/assistido.</p>
    <p><strong>3.6.</strong> Um mesmo CPF ou CNPJ não pode estar associado a mais de um cadastro na Plataforma.</p>

