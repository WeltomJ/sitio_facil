    <h2 class="h5 fw-bold mt-4 mb-3" id="s8">8. Pagamentos, Taxas e Repasses</h2>
    <p><strong>8.1.</strong> Todos os pagamentos realizados na Plataforma são processados por meio da plataforma de pagamentos <strong>Asaas</strong> (Asaas Pagamentos S.A.), estando sujeitos também aos termos e condições desta.</p>
    <p><strong>8.2.</strong> O Cliente pode efetuar o pagamento via <strong>PIX</strong> ou <strong>Cartão de Crédito</strong>. O parcelamento no cartão está sujeito à aprovação da operadora e pode estar sujeito a juros.</p>
    <p><strong>8.3.</strong> O Sítio Fácil cobra uma <strong>taxa de serviço de 3,5% (três vírgula cinco por cento)</strong> sobre o valor da reserva, retida no momento do pagamento. Esse valor remunera os serviços de intermediação, tecnologia e suporte da Plataforma.</p>
    <p><strong>8.4.</strong> O repasse ao Locador será realizado pelo Sítio Fácil <strong>após a conclusão da hospedagem</strong> (data de check-out), deduzida a taxa de serviço, no prazo de até <strong>5 (cinco) dias úteis</strong> contados da data do check-out, para a conta bancária cadastrada pelo Locador.</p>
    <p><strong>8.5.</strong> O Locador é exclusivamente responsável pela declaração e recolhimento dos tributos devidos sobre os valores recebidos, não cabendo ao Sítio Fácil qualquer responsabilidade fiscal relacionada à atividade de locação.</p>
    <p><strong>8.6.</strong> Em caso de cancelamento com direito a reembolso, os valores retornam ao Cliente conforme a Política de Cancelamento disponível em <a href="<?= BASE_URL ?>/cancelamento">sitiofacil.com.br/cancelamento</a>.</p>

