    <p><strong>7.1.</strong> A solicitação de reserva pelo Cliente não cria obrigação de locação. O Contrato de Locação se perfectibiliza somente após a confirmação formal pelo Locador e o pagamento integral pelo Cliente.</p>
    <p><strong>7.2.</strong> O Locador pode recusar a reserva sem necessidade de justificativa, desde que tal recusa não configure discriminação vedada por lei.</p>
    <p><strong>7.3.</strong> Reservas confirmadas bloqueiam o calendário do imóvel para o período correspondente.</p>
    <p><strong>7.4.</strong> O Sítio Fácil não garante a disponibilidade de imóveis específicos para datas específicas.</p>
    <p><strong>7.5.</strong> A avaliação do imóvel e do Locador pelos Clientes é pública e contribui para a reputação da Plataforma. O Sítio Fácil se reserva o direito de remover avaliações que violem estes Termos.</p>

