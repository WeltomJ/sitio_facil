    <p><strong>5.1.</strong> O Locador declara e garante:</p>
    <ul>
        <li>Ser proprietário, possuidor legítimo ou estar devidamente autorizado a locar o imóvel anunciado;</li>
        <li>Que o imóvel está em condições de habitabilidade, higiene e segurança compatíveis com o padrão anunciado;</li>
        <li>Que todas as informações do anúncio (descrição, fotos, capacidade, preço, comodidades, horários de check-in/check-out) são verdadeiras e atualizadas;</li>
        <li>Que o imóvel não possui impedimentos legais ou judiciais para locação;</li>
        <li>Cumprir todas as obrigações tributárias decorrentes dos valores recebidos pela locação, incluindo a emissão de nota fiscal ou recibo quando aplicável, nos termos da legislação fiscal vigente.</li>
    </ul>
    <p><strong>5.2.</strong> O Locador é responsável por confirmar ou recusar reservas dentro do prazo razoável. A ausência de resposta não gera obrigação de aceite.</p>
    <p><strong>5.3.</strong> O Locador deve manter o calendário de disponibilidade atualizado e não pode cancelar reservas já confirmadas, salvo em casos de força maior devidamente comprovados.</p>
    <p><strong>5.4.</strong> O Locador se compromete a não discriminar Clientes por motivos de raça, cor, etnia, religião, orientação sexual, identidade de gênero, origem, condição socioeconômica ou qualquer outra forma vedada pela legislação brasileira.</p>

