    <h2 class="h5 fw-bold mt-4 mb-3" id="s2">2. Natureza da Plataforma</h2>
    <p><strong>2.1.</strong> O Sítio Fácil é uma plataforma de intermediação digital que conecta Clientes e Locadores interessados na locação temporária de imóveis rurais (chácaras, sítios, fazendas e similares).</p>
    <p><strong>2.2.</strong> O Sítio Fácil <u>não é proprietário, administrador, arrendatário, operador ou controlador</u> de nenhum imóvel anunciado na Plataforma. A relação contratual de locação é celebrada exclusivamente entre Cliente e Locador.</p>
    <p><strong>2.3.</strong> O Sítio Fácil não é parte no Contrato de Locação e não se responsabiliza pelo seu cumprimento ou descumprimento por qualquer das partes, ressalvadas as hipóteses expressamente previstas nestes Termos.</p>
    <p><strong>2.4.</strong> A Plataforma atua como prestadora de serviços de tecnologia da informação, conforme disposto no <em>Marco Civil da Internet</em> (Lei 12.965/2014) e na legislação aplicável.</p>

