        <li>Zelar pela conservação do imóvel e de seus bens, responsabilizando-se por danos causados durante o período de locação;</li>
        <li>Observar os horários de check-in e check-out;</li>
        <li>Não realizar festas, eventos ou atividades não autorizadas pelo Locador;</li>
        <li>Respeitar as regras do imóvel comunicadas pelo Locador.</li>
    </ul>
    <p><strong>6.2.</strong> O Cliente é responsável pelo comportamento de todos os hóspedes que acompanhem a reserva, ainda que não cadastrados na Plataforma.</p>

